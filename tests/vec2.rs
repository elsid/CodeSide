use aicup2019::my_strategy::Vec2;

#[test]
fn test_vec2_dot_1() {
    assert_eq!(
        Vec2::new(0.9286244090529895, 0.37102116773977495).dot(Vec2::new(10.833333333342967, 4.328333333333456)),
        11.666001052450037
    );
}

#[test]
fn test_vec2_cos_1() {
    assert_eq!(
        Vec2::new(0.9286244090529895, 0.37102116773977495).cos(Vec2::new(10.833333333342967, 4.328333333333456)),
        1.0
    );
}

#[test]
fn test_vec2_rotation_1() {
    assert_eq!(
        Vec2::new(0.9286244090529895, 0.37102116773977495).rotation(Vec2::new(10.833333333342967, 4.328333333333456)),
        0.0
    );
}
